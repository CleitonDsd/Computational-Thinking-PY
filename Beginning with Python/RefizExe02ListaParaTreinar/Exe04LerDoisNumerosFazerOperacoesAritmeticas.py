#Escreva um algoritmo em Python que recebe dois números inteiros e exibe: a soma desses
#dois números, a multiplicação, a divisão inteira e o resto da divisão inteira.

numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o primeiro número: "))

soma  = numero1 + numero2
print(numero1, " + ", numero2, " = ", soma)

subtracao = numero1 - numero2
print(numero1, " - ", numero2, " = ", subtracao)

multiplicacao = numero1 * numero2
print(numero1, " * ", numero2, " = ", multiplicacao)

divisao = numero1 / numero2
print(numero1, " / ", numero2, " = ", divisao)