# Faça um algoritmo que receba um número e mostre uma mensagem caso este número seja
# maior que 10.

numero = float (input("Digite um número: "))

if numero > 10:
    print(numero , " é maior que 10")
elif numero == 10:
    print(numero , " é igual a 10")
else:
    print(numero , " é menor que 10")



